public class Calculator {

    static int sum(int num1, int num2){
        return num1 + num2;
    }

    static int subtraction(int num1, int num2){
        return num1 - num2;
    }

    static int multiply(int num1, int num2){
        return num1 * num2;
    }

    static double divide(double num1, double num2){
        return num1 / num2;
    }
}

