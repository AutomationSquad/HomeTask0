public class Calculator {

    static int add(int numOne, int numTwo) {
        return numOne + numTwo;

    }

     static int subtract(int numOne, int numTwo) {

        return numOne - numTwo;
    }

    static int multiply(int numOne, int numTwo) {
        return numOne * numTwo;
    }

   static int divide(int numOne, int numTwo) {
       return numOne / numTwo;
    }


    static float addFloat(float numOne, float numTwo) {
        return numOne + numTwo;

    }

    static float subtractFloat(float numOne, float numTwo) {
        return numOne - numTwo;

    }

    static float divideFloat(float numOne, float numTwo) {
        return numOne / numTwo;

    }

    static float multiplyFloat(float numOne, float numTwo) {
        return numOne * numTwo;

    }
}

