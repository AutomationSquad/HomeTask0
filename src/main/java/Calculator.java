public class Calculator {
    public Calculator(){
    }

    public double add(double first, double second){
        return first + second;
    }

    public float subtract(float first, float second){
        return first - second;
    }

    public int multiply(int first, int second){
        return first*second;
    }

    public int divide(int first, int second){
        return first/second;
    }
}
