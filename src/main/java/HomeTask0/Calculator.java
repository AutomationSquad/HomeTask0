package HomeTask0;

public class Calculator {


    public static int addition(int ia, int ib) {

        return ia + ib;
    }

    public static byte subtraction(double da, double db) {

        return (byte) (da - db);
    }


    public static short multiplication(short ba, short bb) {

        return (short) (ba * bb);
    }


    public static float divide(float fa1, float fa2) {
        return fa1 / fa2;

    }


    public static short remainder(short s1, short s2) {

        return (short) (s1 % s2);
    }

}
