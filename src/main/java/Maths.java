
public class Maths {
    // used double in case of using divide operations and prevent inappropriate rounding

    public static double add(double a, double b){
        double result = a + b;
        return  result;
    }

    public static double substract (double a, double b){
        double result = a - b;
        return  result;
    }

    public static double multiply (double a, double b){
        double result = a * b;
        return  result;
    }

    public static double divide (double a, double b){
        double result = a / b;
        return  result;
    }

    public static double square (double a){
        double result = a * a;
        return  result;
    }

}
